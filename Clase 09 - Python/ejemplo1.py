# Declaración y asignación de variables
numero_entero = 5
numero_decimal = 2.5
mensaje = "Hola, Mundo!"
es_verdadero = True

# Operaciones aritméticas
suma = numero_entero + numero_decimal
resta = numero_entero - numero_decimal
multiplicacion = numero_entero * numero_decimal
division = numero_entero / numero_decimal
modulo = numero_entero % 3  # El residuo de la división por 3

# Imprimir resultados
print("Número Entero:", numero_entero)
print("Número Decimal:", numero_decimal)
print("Mensaje:", mensaje)
print("Es Verdadero:", es_verdadero)
print(f"--{es_verdadero}--")

print("Resultado de la Suma:", suma)
print("Resultado de la Resta:", resta)
print("Resultado de la Multiplicación:", multiplicacion)
print("Resultado de la División:", division)
print("Residuo de la División por 3:", modulo)
