    private void ComboUsersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboUsersActionPerformed
        
        int valor = ComboUsers.getSelectedIndex();
        
        nombre.setText(panel1.misUsuarios[valor].getNombre());
        apellido.setText(panel1.misUsuarios[valor].getApellido());
        carne.setText(String.valueOf(panel1.misUsuarios[valor].getCarne()));
        
        if (panel1.misUsuarios[valor].getPath() != null){
            
            Image pImagen = new ImageIcon(panel1.misUsuarios[valor].getPath()).getImage();
            ImageIcon pIcono = new ImageIcon(pImagen.getScaledInstance(lblImagen.getWidth(),lblImagen.getHeight(),Image.SCALE_SMOOTH));
            lblImagen.setIcon(pIcono);
        }else{
            lblImagen.setIcon(null);
        }
    }//GEN-LAST:event_ComboUsersActionPerformed

