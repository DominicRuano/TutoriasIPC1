    private void ImagenbttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImagenbttonActionPerformed

        
        JFileChooser jFileChooser = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("JPG, PNG y GIF","JPG","PNG", "GIF");
        jFileChooser.setFileFilter(filtro);

        int respuesta = jFileChooser.showOpenDialog(null);
        
        if (respuesta == JFileChooser.APPROVE_OPTION){
            
            path = jFileChooser.getSelectedFile().getPath();
            
            Image pImagen = new ImageIcon(path).getImage();
            ImageIcon pIcono = new ImageIcon(pImagen.getScaledInstance(lblImagen.getWidth(),lblImagen.getHeight(),Image.SCALE_SMOOTH));
            
            lblImagen.setIcon(pIcono);
        }
        
    }//GEN-LAST:event_ImagenbttonActionPerformed

